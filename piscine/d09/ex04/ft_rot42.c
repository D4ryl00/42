char	*ft_rot42(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr)
	{
		*ptr = ((*ptr - 33 + 42) % (126 - 33 + 1)) + 33;
		ptr++;
	}
	return (str);
}
