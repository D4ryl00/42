void	ft_putchar(char c);

void	ft_print_numbers(void)
{
	int		i;
	char	c;

	i = 0;
	c = '0';
	while (i < 10)
	{
		ft_putchar(c);
		c = c + 1;
		i = i + 1;
	}
}
